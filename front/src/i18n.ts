const messages = { en, mt };

export const getInitialLang = (): Lang => {
    const stored = localStorage.getItem('tap_lang');
    return stored === 'mt' ? 'mt' : 'en';
};

export const t = (lang: Lang, key: MessageKey): string => messages[lang][key] || messages.en[key];

export const tx = (lang: Lang, key: string, fallback = key): string =>
    messages[lang][key as MessageKey] || messages.en[key as MessageKey] || fallback;
